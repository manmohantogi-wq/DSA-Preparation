// print the sum of two number 


#include<iostream>
using namespace std;
int main()
{
    int a = 10;
    int b = 30;
    int sum = a+b;
    int sub = a-b;
    int multi = a*b;
    int division = a/b;
    int modulo = a%b;
    cout<<"Sum of "<<a<<" and "<<b<<" is :  "<<sum<<endl;
    cout<<"subtraction of "<<a<<" and "<<b<<" is :  "<<sub<<endl;
    cout<<"multiplication of "<<a<<" and "<<b<<" is :  "<<multi<<endl;
    cout<<"division of "<<a<<" and "<<b<<" is :  "<<division<<endl;
    cout<<"modulo of "<<a<<" and "<<b<<" is :  "<<modulo<<endl;
    
    return 0;
}