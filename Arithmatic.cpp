#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"Enter a = ";
    cin>>a;
    cout<<"Enter b = ";
    cin>>b;

    cout<<"Sum of a and b is = "<<(a+b)<<endl;
    cout<<"multiplication of a and b is = "<<(a*b)<<endl;
    cout<<"Subtraction of a and b is = "<<(a-b)<<endl;
    cout<<"division of a and b is = "<<(a/b)<<endl;
    cout<<"modulo of a and b is = "<<(a%b)<<endl;
    return 0;
}